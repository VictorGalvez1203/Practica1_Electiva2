#Tipos de datos en Python
print("int: igual a enteros:")
enteros = 10
print("El tipo de valor es ",type(enteros), "de:", enteros,"\n")

print("string: igual a cadena de texto")
str = "Esto es una cadena de texto"
print("El tipo de valor es ",type(str), "de:", str,"\n")

print("float: Valores decimales")
float = 1.2
print("El tipo de valor es ",type(float), "de:", float,"\n")

print("complex: numeros complejos")
complex = 2 + 3j
print("El tipo de valor es ",type(complex), "de:", complex,"\n")

print("bool: valores booleanos, o sea falso y verdadero")
bool = True
print("El tipo de valor es ",type(bool), "de:", bool,"\n")

print("NoneType: que no hay valor, o sea que esta vacio")
cara = None
print("El tipo de valor es ",type(cara), "de:", cara,"\n")


